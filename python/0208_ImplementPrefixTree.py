class TrieNode:
    def __init__(self):
        self.children = {}
        self.endOfWord = False

class Trie:
    def __init__(self):
        self.root = TrieNode()

    def insert(self, word: str) -> None:
        cur = self.root

        # For each character, create a children of another TrieNode
        # For last character, set the endOfWord to True
        for c in word:
            if c not in cur.children:
                cur.children[c] = TrieNode()
            cur = cur.children[c]
        cur.endOfWord = True

    def search(self, word: str) -> bool:
        cur = self.root

        # Search each character in the Prefix Tree down to the children
        # For last character, the endOfWord must be True
        for c in word:
            if c not in cur.children:
                return False
            cur = cur.children[c]
        return cur.endOfWord

    def startsWith(self, prefix: str) -> bool:
        cur = self.root

        # Search each character in the Prefix Tree down to the children
        # For last character, the endOfWord doesn't have to be True
        for c in prefix:
            if c not in cur.children:
                return False
            cur = cur.children[c]
        return True
        


